package com.company;

public class Boss {
    private int healt;
    private int damage;
    private String defense;

    public String getDefense(String defense) {
        return defense;
    }

    public void setDefense(String defense) {
        this.defense = defense;
    }

    public int getHealt(int healt) {
        return healt;
    }

    public void setHealt(int healt) {
        this.healt = healt;
    }

    public int getDamage(int damage) {
        return damage;
    }

    public void setDamage(int damage) {
        this.damage = damage;
    }
}
